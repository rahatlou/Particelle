      <h3 class="title">Modalita` d'esame</h3>
        <p>
        <b>L'argomento  non puo` essere una tesina dell'esame di FNS II</b>

        <p>
        L'esame si svolge senza l'uso di presentazioni o slide ma solo con carta e penna. 
        si possono portare le pubblicazioni come materiale di supporto
        se utile per la discussione. 

        <p>L'esame include tutto il programma del corso ma senza andare nei dettagli di tutte le singole misure discusse nelle lezioni.
           Per facilitare lo svolgimento dell'esame, il candidato puo` scegliere un argomento da approfondire tra quelli indicati, come il punto di partenza per la prova.

        <p>Al candidato viene chiesto di spiegare
        <ul>
         <li>la rilevanza e l'obiettivo della misura</li>
         <li> quali sono i processi di segnale e di fondo</li>
         <li>quali sono le variabili discriminanti, che distribuzioni hanno</li>
         <li>nel caso di una misura gia` fatta in passato fare un confronto e spiegare come e perche` la misura presa 
             in considerazione e` migliore di quella(e) precedente</li>
         <li>nel caso di misure nuove, spiegare perche` non fosse stato possibile farla in precedenza</li>
         <li>discutere i principali limiti ed incertezze sperimentali e teoriche che influenzano la misura</li>
        </ul>
